package com.example.mobitest.view;


public class Category{

	String episode_title;
	String episode_order;
	String view_cost;
	String gold_ink_only;
	String rating_avg;
	String publishing_date;

	public String getEpisode_title() {
		return episode_title;
	}
	public void setEpisode_title(String episode_title) {
		this.episode_title = episode_title;
	}
	public String getEpisode_order() {
		return episode_order;
	}
	public void setEpisode_order(String episode_order) {
		this.episode_order = episode_order;
	}
	public String getView_cost() {
		return view_cost;
	}
	public void setView_cost(String view_cost) {
		this.view_cost = view_cost;
	}
	public String getGold_ink_only() {
		return gold_ink_only;
	}
	public void setGold_ink_only(String gold_ink_only) {
		this.gold_ink_only = gold_ink_only;
	}
	public String getRating_avg() {
		return rating_avg;
	}
	public void setRating_avg(String rating_avg) {
		this.rating_avg = rating_avg;
	}
	public String getPublishing_date() {
		return publishing_date;
	}
	public void setPublishing_date(String publishing_date) {
		this.publishing_date = publishing_date;
	}




}
